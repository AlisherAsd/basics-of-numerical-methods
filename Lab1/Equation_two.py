
# Для уравнения 2 (Метод хорд):

# Определяем функцию f(x) = 5^x - 6x - 7
def f(x):
    return 5**x - 6*x - 7

# Задаем начальные границы интервала
a, b = 1, 2
# Задаем точность вычисления
tol = 1e-6
# Максимальное количество итераций
max_iter = 100

# Метод хорд для поиска корня
for i in range(max_iter):
    # Вычисляем значения функции на концах интервала
    fa, fb = f(a), f(b)
    # Находим точку пересечения хорды с осью x
    x_new = a - fa * (b - a) / (fb - fa)
    # Проверяем условие сходимости
    if abs(f(x_new)) < tol:
        break
    # Уточняем границы интервала
    if f(x_new) * fa < 0:
        b = x_new
    else:
        a = x_new

# Выводим результат
print(f"Корень: {x_new}, Итерации: {i+1}")